class Operator{
     public static void main(String[] args){
      int x = 8;
      int y = -9;
      boolean a = true;
      boolean b = false;
      System.out.println(x++);
      System.out.println(++x);
      System.out.println(x--);
      System.out.println(--x);
      System.out.println("Operator,Opp boolean, ");
      System.out.println(x++ + ++x);
      System.out.println(x++ + x++);
      System.out.println(~x);
      System.out.println(~y);
      System.out.println(!a);
      System.out.println(!b);
}
}