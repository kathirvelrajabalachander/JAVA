import java.util.*;

class Bitwise{
   public static void main(String[] args){
     Scanner sc = new Scanner(System.in);
     System.out.println("Enter A : ");
     System.out.println("Enter B : ");
     int a = sc.nextInt();
     int b = sc. nextInt();
     System.out.println(a<<b);
     System.out.println(a<<b);
     System.out.println(a>>b);
     System.out.println(a>>b);
     System.out.println(a>>>b);
     
}
}